    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        String tempName = inputName.getText();
        String tempEmail = inputEmail.getText();
        String tempLevel = checkboxCarrera.getSelectedItem().toString();
        String tempCarrera = checkboxLevel.getSelectedItem().toString();
        String valide = this.valideInforForm(tempName, tempEmail, tempLevel, tempCarrera);
        if ((valide.isEmpty())) {
            Alumns tempAlumns = new Alumns(tempName, tempEmail, tempLevel, tempCarrera);
            this.alumnsCollection.add(tempAlumns);
            if (ControllerFIles.createFile(alumnsCollection)) {
                this.cleanTexfields();;
                JOptionPane.showMessageDialog(null, "Alumno registrado");
                try {
                    this.initTableAlumns();
                } catch (IOException ex) {
                    Logger.getLogger(uiFormulario.class.getName()).log(Level.SEVERE, null, ex);
                }
                try {
                    ControllerFIles.getCollectionAlumns();
                } catch (Exception e) {
                    System.out.println(e.toString());
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, valide);
        }
    }//GEN-LAST:event_jButton1MouseClicked

