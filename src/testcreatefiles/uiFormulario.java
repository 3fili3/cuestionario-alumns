    public void initTableAlumns() throws IOException {
        this.alumnsCollection = ControllerFIles.getCollectionAlumns();
        Object[][] Alumns = new Object[this.alumnsCollection.size()][4];
        String[] headerColumns = new String[4];
        headerColumns[0] = "Nombre";
        headerColumns[1] = "Correo Electronico";
        headerColumns[2] = "Carrera";
        headerColumns[3] = "Semestre";
        this.x = 0;
        this.alumnsCollection.stream().forEach(alumn -> {
            Alumns[x][0] = alumn.getName();
            Alumns[x][1] = alumn.getEmail();
            Alumns[x][3] = alumn.getCarrera();
            Alumns[x][2] = alumn.getLevel();
            this.x += 1;
        });

        DefaultTableModel modelTable = new DefaultTableModel(Alumns, headerColumns);
        jTableAlumns.setModel(modelTable);
    }

